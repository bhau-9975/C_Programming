void Display(int iNo)
{
    if (iNo < 0) // Updator 
    {
        iNo = -iNo;
    }
    int i = 0;
    i = iNo;
    while (i >= 1)
    {
        printf("%d\n", i);

        i--;
    }
}
////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iFrequency = 0;

    printf("Enter the fraquency : ");
    scanf("%d", &iFrequency);

    Display(iFrequency);
}