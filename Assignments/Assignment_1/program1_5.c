#include <stdio.h>
////////////////////////////////////////////////////////////////////////////////////////
//
//  Fanction Name   :   Display
//  Descrption      :   It is used to print N times * on screen 
//  Input           :   5
//  Output          :   *   *   *   *   *
//  Author          :   Bhau Soma Pise
//  Date            :   18-10-2025
//
////////////////////////////////////////////////////////////////////////////////////////

void Display(int iFrequency)
{
   
    if (iFrequency < 0) // Updator
    {
        iFrequency = -iFrequency;
    }

    int i = 0;

    for (i = 1; i <= iFrequency; i++)
    {
        printf("%d : *\n",i);
    }
}
////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point of the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the frequency : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}