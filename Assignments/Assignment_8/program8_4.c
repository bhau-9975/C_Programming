#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   Table
//  Descrption      :   Print table of number
//  Input           :   5            
//  Output          :   5   10  15  20  25  30  35  40  45  50  
//  Author          :   Bhau Soma Pise
//  Date            :   19-10-2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////

int Table(int iNo)
{
    int iCnt = 0, iTab = 0;

     if (iNo < 0)   // Updator
    {
        iNo = -iNo;
    }

    for (iCnt = 1; iCnt <= 10; iCnt++)
    {
        iTab = iNo * iCnt;
        printf("%d\t", iTab);
    }
}

// Time complexity : O(10)

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    Table(iValue);
    return 0;
}
