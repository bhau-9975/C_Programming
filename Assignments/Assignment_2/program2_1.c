#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////////////
//
//  Fanction Name   :   Display
//  Descrption      :   It is used to print N times * on screen using while loop (Forword order of loop)
//  Input           :   5
//  Output          :   *   *   *   *   *
//  Author          :   Bhau Soma Pise
//  Date            :   18-10-2025
//
////////////////////////////////////////////////////////////////////////////////////////
void Display(int iNo)
{
    int iCnt = 0;

    if (iNo < 0) // updator
    {
        iNo = -iNo;
    }

    iCnt = 1;
    while (iCnt <= iNo)
    {
        printf("%d : * \n", iCnt);
        iCnt++;
    }
}
////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    printf("Enter the number : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}