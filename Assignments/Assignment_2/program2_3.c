#include <stdio.h>
///////////////////////////////////////////////////////////////////////////////////////////
//
//  Fanction Name   :   Display
//  Descrption      :   It accepts the number from user, if number is less than 10 then print "Hello" otherwise print "Demo"
//  Input           :   int
//  Output          :   Hello or Demo
//  Author          :   Bhau Soma Pise
//  Date            :   18-10-2025
//
///////////////////////////////////////////////////////////////////////////////////////////
void Display(int iNo)

{
    if (iNo < 10)
    {
        printf("Hello...");
    }
    else
    {
        printf("Demo...");
    }
}
////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    printf("Enter the number : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}