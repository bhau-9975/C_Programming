//
//  Write a program which accepts the radius of circle from user and calculate its area
//  Value of PI=3.14 and (Area = PI * Radius * Radius)
///////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   AreaOfCircle
//  Descrption      :   Calculates the area of circle
//  Input           :   5.3     
//  Output          :   88.2025
//  Author          :   Bhau Soma Pise
//  Date            :   25-10-2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////

float AreaOfCircle(float fRad)
{
    float PI = 3.14, fArea = 0.0;

    fArea = (PI * fRad * fRad);
    
    return fArea;
}

// Time complexity : O(1)    

int main()
{
    float fValue = 0.0, fRet = 0.0;

    printf("Enter the number : ");
    scanf("%f", &fValue);

    fRet = AreaOfCircle(fValue);
    printf("The area circle is : %.4f", fRet);
    return 0;
}
