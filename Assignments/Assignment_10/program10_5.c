//  Write a program which accepts the area in square feet and convert it into square meter.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   AreaInSqMeter
//  Descrption      :   Converts area from square feet to square meter
//  Input           :   5 
//  Output          :   0.464515
//  Author          :   Bhau Soma Pise
//  Date            :   25-10-2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////

float AreaInSqMeter(float fSquareFeet)
{
    float fSquareMeter = 0.0;

    fSquareMeter = (fSquareFeet * 0.0929);

    return fSquareMeter;
}

// Time complexity : O(1)

int main()
{
    float fValue = 0, fRet = 0;

    printf("Enter the area in square feet : ");
    scanf("%f", &fValue);

    fRet = AreaInSqMeter(fValue);
    printf("The area in square meter is: %.4f.", fRet);
    return 0;
}
