#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   FhToCelcius
//  Descrption      :   Converts temp from Fehrenheit to Celcius
//  Input           :   10  
//  Output          :   -12.2222    ((10-32)*5/9)
//  Author          :   Bhau Soma Pise
//  Date            :   25-10-2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////

float FhToCelcius(float fFehrenheit)
{
    float fCelcius = 0.0;

    fCelcius = (fFehrenheit - 32) * 5 / 9;

    return fCelcius;
}

// Time complexity : O(1)

int main()
{
    float fValue = 0, fRet = 0;

    printf("Enter the temprature in Fehrenheit : ");
    scanf("%f", &fValue);

    fRet = FhToCelcius(fValue);
    printf("The temperature is: %.4f Celcius.", fRet);
    return 0;
}
