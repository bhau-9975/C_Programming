#include <stdio.h>
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   KMToMeter
//  Descrption      :   Converts KM to Meter
//  Input           :   5     
//  Output          :   5000
//  Author          :   Bhau Soma Pise
//  Date            :   25-10-2025
//
//////////////////////////////////////////////////////////////////////////////////////////////////
float KMtoMeter(float fKiloMeter)
{
    float fMeter = 0.0;

    fMeter = (fKiloMeter * 1000);

    return fMeter;
}

// Time complexity : O(1)

int main()
{
    float fValue = 0, fRet = 0;

    printf("Enter the distance in Kilo-Meter : ");
    scanf("%f", &fValue);

    fRet = KMtoMeter(fValue);
    printf("The distance in meter is : %.4f Meters.", fRet);
    return 0;
}
